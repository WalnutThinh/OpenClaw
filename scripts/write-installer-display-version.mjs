/** Writes NSIS !define for installer branding from root package.json `version` (semver x.y.z). */
import { readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'))
const v = String(pkg.version ?? '0.0.0')

const out = join(root, 'build/installer-display-version.nsh')
const body = `; Auto-generated by scripts/write-installer-display-version.mjs — do not edit\n!define OPENCLAW_INSTALLER_DISPLAY_VERSION "${v}"\n`
writeFileSync(out, body, 'utf8')
console.log('[write-installer-display-version]', v, '→', out)
